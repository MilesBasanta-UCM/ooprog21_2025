
    public class Comments {
    public static void main(String[] args) {
        
        // 1. Single-line comment
        System.out.println("This is an example of a single-line comment.");
        
        /* 
           2. Multi-line comment
           This type of comment can span across
           multiple lines and is ignored by the compiler.
        */
        System.out.println("This is an example of a multi-line comment.");
        
        /**
         * 3. Documentation comment (Javadoc comment)
         * Used to describe classes, methods, and variables.
        */
        System.out.println("This is an example of a documentation (Javadoc) comment.");
    }
}